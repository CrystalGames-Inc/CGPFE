package God.Creation.Entity.Mandatory;

public class EntityAttributes {
    public int Strength;
    public int Dexterity;
    public int Constitution;
    public int Intelligence;
    public int Wisdom;
    public int Charisma;
    public int MoveSpeed;

    public EntityAttributes(int strength, int dexterity, int constitution, int intelligence, int wisdom, int charisma, int moveSpeed) {
        Strength = strength;
        Dexterity = dexterity;
        Constitution = constitution;
        Intelligence = intelligence;
        Wisdom = wisdom;
        Charisma = charisma;
        MoveSpeed = moveSpeed;
    }
}
