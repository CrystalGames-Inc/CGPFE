package Engine.God.Creation.Entity.Additional;

public class EntityWallet {
    public double CopperPieces;
    public double SilverPieces;
    public double GoldPieces;
    public double PlatinumPieces;

    public EntityWallet(double copperPieces, double silverPieces, double goldPieces, double platinumPieces){
        CopperPieces = copperPieces;
        SilverPieces = silverPieces;
        GoldPieces = goldPieces;
        PlatinumPieces = platinumPieces;
    }
}
