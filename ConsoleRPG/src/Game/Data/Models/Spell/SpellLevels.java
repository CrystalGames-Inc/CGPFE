package Game.Data.Models.Spell;

public class SpellLevels {
    public int Barbarian;
    public int Bard;
    public int Cleric;
    public int Druid;
    public int Fighter;
    public int Monk;
    public int Paladin;
    public int Ranger;
    public int Rogue;
    public int Sorcerer;
    public int Wizard;

    public SpellLevels(int barbarian, int bard, int cleric, int druid, int fighter, int monk, int paladin, int ranger, int rogue, int sorcerer, int wizard) {
        Barbarian = barbarian;
        Bard = bard;
        Cleric = cleric;
        Druid = druid;
        Fighter = fighter;
        Monk = monk;
        Paladin = paladin;
        Ranger = ranger;
        Rogue = rogue;
        Sorcerer = sorcerer;
        Wizard = wizard;
    }
}
